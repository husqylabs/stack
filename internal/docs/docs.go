// Package docs renders the CLI documentation from the live cobra command tree.
// It is the single source for both the `go generate` writer (tools/docgen) and
// the drift test, so the generator and the staleness check can never disagree.
// Everything user-facing flows from internal/branding.
package docs

import (
	"fmt"
	"sort"
	"strings"

	"github.com/husqylabs/stack/internal/branding"
	"github.com/spf13/cobra"
	"github.com/spf13/pflag"
)

const generatedNote = "<!-- Generated by `go generate ./...` (tools/docgen). Do not edit by hand. -->"

// Index renders the docs home page (docs/index.md), the landing page of the site.
func Index(root *cobra.Command) string {
	b := branding.B
	var sb strings.Builder
	fmt.Fprintf(&sb, "# %s\n\n%s\n\n", b.Title, generatedNote)
	fmt.Fprintf(&sb, "%s\n\n", root.Short)
	fmt.Fprintf(&sb, "`%s` manages **stacked pull requests** entirely client-side — no "+
		"backend server. The branch DAG is synced across teammates by embedding it as a "+
		"hidden comment in each PR description.\n\n", b.Name)

	sb.WriteString("## Get started\n\n")
	sb.WriteString("- **[Cheat sheet](cheatsheet.md)** — one-page quick reference and workflow recipes.\n")
	sb.WriteString("- **[Command reference](commands.md)** — every command, flag, and default.\n\n")

	sb.WriteString("## Install\n\n```sh\ngo build -o " + b.Name + " .\n```\n\n")

	fmt.Fprintf(&sb, "## Quickstart\n\n```sh\n"+
		"%[1]s %[2]s feature-a      # branch off the trunk and track it\n"+
		"# ...commit work...\n"+
		"%[1]s %[2]s feature-b      # stack feature-b on feature-a\n"+
		"# ...commit work...\n\n"+
		"export %[4]s=$(gh auth token)\n"+
		"%[1]s %[3]s --owner ORG --repo REPO   # push the stack and open PRs\n"+
		"```\n", b.Name, b.CmdStart, b.CmdSubmit, b.Env("TOKEN"))
	return sb.String()
}

